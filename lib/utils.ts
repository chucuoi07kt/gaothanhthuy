export function slugifyVietnamese(text: string): string {
  if (!text) return '';
  let result = '';
  for (const char of text) {
    result += VIETNAMESE_MAP[char] ?? char;
  }
  return result
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/[\s_]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-+|-+$/g, '');
}
